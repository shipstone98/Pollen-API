namespace Shipstone.Pollen.Api.Core.Pollen;

/// <summary>
/// Defines constants for pollen categories.
/// </summary>
public enum PollenCategory
{
    /// <summary>
    /// Represents no pollen category.
    /// </summary>
    None = 0,

    /// <summary>
    /// Represents a very low pollen category.
    /// </summary>
    VeryLow,

    /// <summary>
    /// Represents a low pollen category.
    /// </summary>
    Low,

    /// <summary>
    /// Represents a moderate pollen category.
    /// </summary>
    Moderate,

    /// <summary>
    /// Represents a high pollen category.
    /// </summary>
    High,

    /// <summary>
    /// Represents a very high pollen category.
    /// </summary>
    VeryHigh
}
